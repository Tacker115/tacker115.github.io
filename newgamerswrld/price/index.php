	<script type="text/javascript">
		
		function switchClasses() {
		if(window.outerWidth  < 1400) {
			$('.day-title-wrap').addClass('none');
			$('.day-title-mobile').removeClass('none');
		}
		else{
			$('.day-title-wrap').removeClass('none');
			$('.day-title-mobile').addClass('none');
			}
		}
		
		$(document).ready(function() {
			switchClasses();
			$('.tab-item').click(function() {
			   var id = $(this).attr('data-tab'),
				   content = $('.tab-content-item[data-tab="'+ id +'"]');
			   
			   $('.tab-item.active').removeClass('active');
			   $(this).addClass('active');
			   
			   $('.tab-content-item.active').removeClass('active');
			   content.addClass('active');
			});
		});
		
		
